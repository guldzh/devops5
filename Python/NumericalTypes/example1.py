# In the farm we have 35 cows , 23 chickens,and 40 ducks.
# Create a program to calculate total number of 
# legs in the farm
# Requirements create a variable for cows, chickens and ducks , and create variables
# for their number of legs. 
# print "We have 'result' legs in the farm"

cows=35
chicken=23
ducks=40

chickenLegs=chicken*2
duckLegs=ducks*2
cowsLegs=cows*4

totalLegs=cowsLegs+duckLegs+chickenLegs;

print("We have", totalLegs, "legs in the farm")
